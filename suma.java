// Lógica para sumar dos números en Java

public class suma {

    //Método que devuelve la suma de dos números.
    public static int sumar(int a, int b) {
        return a + b;
    }

    // Método principal para probar la función
    public static void main(String[] args) {
        int resultado = sumar(2, 3);
        System.out.println("Prueba de suma:");
        System.out.println("2 + 3 = " + resultado);
    }
}
